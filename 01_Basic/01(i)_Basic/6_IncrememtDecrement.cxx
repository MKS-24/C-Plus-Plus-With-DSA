# include<iostream>
using namespace std;
int main(){
    // x++ post increment
    // ++x pre increment

    int x=1;
    cout<<x++<<endl;// likh ker berhadu
    cout<<x<<endl;
    cout<<++x<<endl;// barhake likhu

    // x-- post decrement
    // --x pre decrement

    int y=3;
    cout<<y--<<endl;// likh ker ghatao
    cout<<y<<endl;
    cout<<--y<<endl;// ghataker  likhu

    return 0;
}